#include "libft.h"

int		ft_isablank(char *s)
{
	int i;
	int len;

	i = 0;
	len = ft_strlen((char*)s);
	while (s[i] == ' ' || s[i] == '\t' || s[i] == '\n')
		i++;
	if (len != i)
		return (0);
	return (-1);
}
