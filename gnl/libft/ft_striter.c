#include "libft.h"

void	ft_striter(char *s, void (*f)(char *))
{
	int i;
	int len;

	if (!s || !f)
		return ;
	i = 0;
	if (s)
	{
		len = ft_strlen(s);
		while (i < len)
		{
			f(&s[i]);
			i++;
		}
	}
}
